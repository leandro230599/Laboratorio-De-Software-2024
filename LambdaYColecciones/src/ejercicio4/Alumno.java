package ejercicio4;

public class Alumno {
	private int nro_alumno;
	private String nombre;
	private String apellido;
	private int edad;
	private String materia_aprobada;
	private double nota_aprobacion;
	
	
	public int getNro_alumno() {
		return nro_alumno;
	}
	public void setNro_alumno(int nro_alumno) {
		this.nro_alumno = nro_alumno;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public int getEdad() {
		return edad;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	public String getMateria_aprobada() {
		return materia_aprobada;
	}
	public void setMateria_aprobada(String materia_aprobada) {
		this.materia_aprobada = materia_aprobada;
	}
	public double getNota_aprobacion() {
		return nota_aprobacion;
	}
	public void setNota_aprobacion(double nota_aprobacion) {
		this.nota_aprobacion = nota_aprobacion;
	}
	
}
